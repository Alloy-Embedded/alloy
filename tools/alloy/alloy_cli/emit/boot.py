"""Emit second-stage boot payloads (RP2040 boot2).

The payload bytes are a FACT from chip data (CRC-checked by the
alloy-devices lints before we ever get here); this emitter only formats
them into a C array placed in the .boot2 section, which the generated
linker script keeps at the very start of flash.
"""

from __future__ import annotations

from typing import Any

_C_BANNER = "/* GENERATED by alloy — DO NOT EDIT (fix the data or the emitter and regenerate) */\n"


def emit_boot2(chip: dict[str, Any]) -> str:
    boot = chip["boot"]
    payload = bytes.fromhex(boot["payload_hex"])
    rows = []
    for i in range(0, len(payload), 12):
        rows.append("    " + ", ".join(f"0x{b:02X}" for b in payload[i:i + 12]) + ",")
    body = "\n".join(rows)
    return f"""{_C_BANNER}/* {boot['kind']} — {boot.get('description', '')}
 * License: {boot['license']}
 */

__attribute__((used, section(".boot2")))
const unsigned char g_boot2[{boot['size']}] = {{
{body}
}};
"""
