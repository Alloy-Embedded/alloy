"""Shared emitter helpers. Deterministic output: no timestamps, sorted iteration."""

from __future__ import annotations

from typing import Any

BANNER = "// GENERATED by alloy — DO NOT EDIT (fix the data or the emitter and regenerate)\n"

# Signal names accepted from chip data, emitted as alloy::signal::<name>.
# A name missing from the C++ enum fails the build loudly; a name missing
# here fails generation — facts fail as early as possible.
SIGNALS = {
    "tx", "rx", "cts", "rts", "ck",
    "sck", "miso", "mosi", "cs",
    "scl", "sda",
    "in", "out",
}

CLOCK_NODES = {"sysclk", "ahb", "apb"}


class EmitError(RuntimeError):
    pass


def hex32(value: int) -> str:
    return f"0x{value:08X}u"


def cpp_ip_namespace(ip_key: str) -> tuple[str, str]:
    """'st/gpio_v2' -> ('st', 'gpio_v2')."""
    vendor, ip = ip_key.split("/", 1)
    return vendor, ip


def register_by_name(ip_doc: dict[str, Any], name: str) -> dict[str, Any]:
    for reg in ip_doc["registers"]:
        if reg["name"] == name:
            return reg
    raise EmitError(f"{ip_doc['vendor']}/{ip_doc['ip']}: no register {name}")


def field_lookup(reg: dict[str, Any], name: str) -> tuple[int, int]:
    """Resolve a field (including repeat-expanded names like MODER5) to (bit, width)."""
    for f in reg.get("fields", []):
        width = f.get("width", 1)
        if f["name"] == name:
            return f["bit"], width
        rep = f.get("repeat")
        if rep and name.startswith(f["name"]):
            suffix = name[len(f["name"]):]
            if suffix.isdigit() and int(suffix) < rep["count"]:
                return f["bit"] + int(suffix) * rep["stride"], width
    raise EmitError(f"register {reg['name']}: no field {name}")
