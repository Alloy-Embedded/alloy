"""Emit the linker script from the chip's memory map.

The old database shipped family-max sizes that would have produced broken
linker scripts; the alloy-devices plausibility lints run before generation,
so by the time we get here the sizes have been checked against the part
number.
"""

from __future__ import annotations

from typing import Any

from .common import EmitError

_BANNER = "/* GENERATED by alloy — DO NOT EDIT (fix the data or the emitter and regenerate) */\n"


def emit_linker_script(chip: dict[str, Any], arch_ns: str = "cortex_m") -> str:
    if arch_ns == "xtensa":
        return _emit_xtensa_linker(chip)
    flash = next((m for m in chip["memories"] if m["kind"] == "flash"), None)
    ram = next((m for m in chip["memories"] if m["kind"] == "ram"), None)
    if flash is None or ram is None:
        raise EmitError("chip data must declare flash and ram memories")

    boot_section = ""
    if "boot" in chip:
        boot_section = """    .boot2 :
    {
        KEEP(*(.boot2))
    } > FLASH

"""

    return f"""{_BANNER}/* Chip: {chip['vendor']}/{chip['part']} */

MEMORY
{{
    FLASH (rx)  : ORIGIN = {flash['base']}, LENGTH = {flash['size']}
    RAM   (rwx) : ORIGIN = {ram['base']}, LENGTH = {ram['size']}
}}

ENTRY(Reset_Handler)

_estack = ORIGIN(RAM) + LENGTH(RAM);

SECTIONS
{{
{boot_section}    .isr_vector :
    {{
        KEEP(*(.isr_vector))
    }} > FLASH

    .text :
    {{
        *(.text*)
        *(.rodata*)
        . = ALIGN(4);
    }} > FLASH

    .init_array :
    {{
        __preinit_array_start = .;
        KEEP(*(.preinit_array*))
        __preinit_array_end = .;
        __init_array_start = .;
        KEEP(*(SORT(.init_array.*)))
        KEEP(*(.init_array*))
        __init_array_end = .;
    }} > FLASH

    .fini_array :
    {{
        __fini_array_start = .;
        KEEP(*(SORT(.fini_array.*)))
        KEEP(*(.fini_array*))
        __fini_array_end = .;
    }} > FLASH

    _sidata = LOADADDR(.data);

    .data :
    {{
        . = ALIGN(4);
        _sdata = .;
        *(.data*)
        . = ALIGN(4);
        _edata = .;
    }} > RAM AT> FLASH

    .bss :
    {{
        . = ALIGN(4);
        _sbss = .;
        *(.bss*)
        *(COMMON)
        . = ALIGN(4);
        _ebss = .;
    }} > RAM

    /DISCARD/ :
    {{
        *(.ARM.exidx*)
        *(.ARM.extab*)
    }}
}}
"""


def _emit_xtensa_linker(chip: dict[str, Any]) -> str:
    """Classic-ESP32 XIP layout, byte-faithful to the old repo's
    hardware-validated esp32.ld (IROM/DROM MMU-mapped by the resident IDF
    bootloader; +0x10 origins leave room for the image segment header;
    literals placed before text for l32r negative offsets)."""
    mem = {m["name"]: m for m in chip["memories"]}
    for req in ("irom", "drom", "iram", "dram", "vectors"):
        if req not in mem:
            raise EmitError(f"xtensa linker needs memory '{req}' in chip data")

    return f"""{_BANNER}/* Chip: {chip['vendor']}/{chip['part']} — XIP app under resident 2nd-stage bootloader */

ENTRY(call_start_cpu0)

MEMORY
{{
    DROM (r)   : ORIGIN = {mem['drom']['base']}, LENGTH = {mem['drom']['size']}
    IROM (rx)  : ORIGIN = {mem['irom']['base']}, LENGTH = {mem['irom']['size']}
    VECTORS (rx) : ORIGIN = {mem['vectors']['base']}, LENGTH = {mem['vectors']['size']}
    IRAM (rwx) : ORIGIN = {mem['iram']['base']}, LENGTH = {mem['iram']['size']}
    DRAM (rw)  : ORIGIN = {mem['dram']['base']}, LENGTH = {mem['dram']['size']}
}}

_stack_top = ORIGIN(DRAM) + LENGTH(DRAM);

SECTIONS
{{
    /* XEA2 relocatable vector table + level-1 handlers: 1 KiB-aligned,
     * loaded to IRAM by the resident bootloader, pointed at by VECBASE. */
    .alloy.vectors : ALIGN(1024)
    {{
        KEEP(*(.alloy.vectors))
    }} > VECTORS

    .rodata : ALIGN(4)
    {{
        *(.rodata .rodata.*)
        *(.rodata1)
        . = ALIGN(4);
    }} > DROM

    .text : ALIGN(4)
    {{
        /* Literal pools must precede their l32r users */
        *(.literal .literal.*)
        *(.text .text.*)
        . = ALIGN(4);
    }} > IROM

    .init_array : ALIGN(4)
    {{
        __preinit_array_start = .;
        KEEP(*(.preinit_array*))
        __preinit_array_end = .;
        __init_array_start = .;
        KEEP(*(SORT(.init_array.*)))
        KEEP(*(.init_array*))
        __init_array_end = .;
        __fini_array_start = .;
        __fini_array_end = .;
    }} > IROM

    /* No AT> flash LMA and no _sidata on xtensa: esptool images sections
     * by VMA and the resident bootloader loads .data straight into DRAM —
     * a flash LMA would be an unloadable trap (see startup.cpp note). */
    .data : ALIGN(4)
    {{
        *(.data .data.*)
        . = ALIGN(4);
    }} > DRAM

    .bss (NOLOAD) : ALIGN(4)
    {{
        _sbss = .;
        *(.bss .bss.*)
        *(COMMON)
        . = ALIGN(4);
        _ebss = .;
    }} > DRAM
}}
"""
