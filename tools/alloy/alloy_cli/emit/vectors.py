"""Emit the Cortex-M vector table (C file — address constants in static
initializers are cleaner in C than C++).

Facts (entry order, IRQ names/numbers) come from chip data; the reset
handler behavior lives hand-written in src/alloy/arch/cortex_m/startup.cpp.
"""

from __future__ import annotations

from typing import Any

from .common import EmitError, register_by_name

_C_BANNER = "/* GENERATED by alloy — DO NOT EDIT (fix the data or the emitter and regenerate) */\n"

# ARMv6-M/v7-M system exception slots 1..15 (slot 0 is the initial SP).
_SYSTEM_SLOTS = [
    "Reset_Handler", "NMI_Handler", "HardFault_Handler",
    None, None, None, None, None, None, None,
    "SVC_Handler", None, None, "PendSV_Handler", "SysTick_Handler",
]


def emit_vector_table(chip: dict[str, Any]) -> str:
    irqs = sorted(chip["interrupts"], key=lambda i: i["number"])
    max_irq = irqs[-1]["number"]
    by_number = {i["number"]: i["name"] for i in irqs}
    if len(by_number) != len(irqs):
        raise EmitError("duplicate IRQ numbers in chip data")

    weak_decls: list[str] = [
        f"void {name}(void) __attribute__((weak, alias(\"Default_Handler\")));"
        for name in _SYSTEM_SLOTS
        if name is not None and name != "Reset_Handler"
    ]
    entries: list[str] = ["    (vector_t)&_estack,"]
    for slot in _SYSTEM_SLOTS:
        entries.append(f"    {slot}," if slot else "    0,")

    # Per-IRQ wrappers: WEAK functions (not aliases) whose body forwards the
    # line number to the hand-written slot dispatcher. A strong
    # <NAME>_IRQHandler anywhere in the link still overrides its wrapper —
    # the zero-latency expert path stays available.
    wrappers: list[str] = []
    for number in range(max_irq + 1):
        name = by_number.get(number)
        if name is None:
            entries.append(f"    0, /* IRQ{number} reserved */")
            continue
        handler = f"{name}_IRQHandler"
        wrappers.append(
            f"__attribute__((weak)) void {handler}(void) {{ alloy_irq_dispatch({number}u); }}"
        )
        entries.append(f"    {handler}, /* IRQ{number} */")

    decls = "\n".join(weak_decls)
    wrapper_block = "\n".join(wrappers)
    table = "\n".join(entries)
    slot_count = max_irq + 1
    return f"""{_C_BANNER}/* Chip: {chip['vendor']}/{chip['part']} — {len(irqs)} IRQ lines */

typedef void (*vector_t)(void);

extern unsigned int _estack;

void Reset_Handler(void);

void Default_Handler(void) {{
    for (;;) {{
    }}
}}

/* RAM slot table consumed by alloy::irq (src/alloy/irq.hpp) and served by
 * alloy_irq_dispatch (src/alloy/arch/cortex_m/irq_dispatch.cpp). */
struct alloy_irq_slot {{
    void (*fn)(void *);
    void *ctx;
}};
struct alloy_irq_slot g_alloy_irq_slots[{slot_count}];
const unsigned short g_alloy_irq_slot_count = {slot_count};

void alloy_irq_dispatch(unsigned n);

{decls}

{wrapper_block}

__attribute__((used, section(".isr_vector")))
const vector_t g_vector_table[] = {{
{table}
}};
"""

def emit_xtensa_irq_data(chip: dict[str, Any], registers: dict[str, dict[str, Any]]) -> str:
    """Slot table + interrupt-matrix facts for the Xtensa irq layer.

    No vector table here — vectors.S owns the XEA2 table; VECBASE points at
    it. This TU carries only the DATA the hand-written arch code needs:
    the slot array (sized by the source count) and the resolved address of
    the DPORT PRO_INTR_MAP array.
    """
    irqs = sorted(chip["interrupts"], key=lambda i: i["number"])
    slot_count = irqs[-1]["number"] + 1

    intmap = None
    for name, periph in chip["peripherals"].items():
        ip_doc = registers[periph["ip"]]
        for reg in ip_doc["registers"]:
            if reg["name"] == "PRO_INTR_MAP":
                addr = int(periph["base"], 16) + int(reg["offset"], 16)
                stride = reg["array"]["stride"]
                count = reg["array"]["count"]
                intmap = (addr, stride, count)
    if intmap is None:
        raise EmitError("xtensa chip has interrupts but no peripheral models PRO_INTR_MAP")
    if intmap[2] < slot_count:
        raise EmitError("interrupt sources exceed the PRO_INTR_MAP array count")

    return f"""{_C_BANNER}/* Chip: {chip['vendor']}/{chip['part']} — {len(irqs)} interrupt-matrix sources */

/* RAM slot table consumed by alloy::irq; served by alloy_irq_dispatch
 * (src/alloy/arch/xtensa/irq_ctrl.cpp) via the cpu-int -> source map. */
struct alloy_irq_slot {{
    void (*fn)(void *);
    void *ctx;
}};
struct alloy_irq_slot g_alloy_irq_slots[{slot_count}];
const unsigned short g_alloy_irq_slot_count = {slot_count};

/* DPORT PRO_INTR_MAP array (one register per source), resolved from data. */
const unsigned long g_alloy_intmap_addr = 0x{intmap[0]:08X}UL;
const unsigned short g_alloy_intmap_stride = {intmap[1]};
"""
